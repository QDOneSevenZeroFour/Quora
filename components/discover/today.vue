height="388" class="origin_image zh-lightbox-thumb" width="594" data-original="https://pic4.zhimg.com/v2-54f82bc582341bf8df3be1fbf937eaa7_r.jpg"&gt;&lt;p&gt;冯远征老师&lt;b&gt;一天24小时至少三分之二都献给了表演&lt;/b&gt;，给演员训练台词，给学生做讲课，自己还要排练演出。&lt;/p&gt;&lt;p&gt;媒体上总写他批评这个，批评那个，什么三大表演体系是骗人的，小鲜肉演技不好老师也有责任之类的爆炸宣言，追根溯源，这其实是一个老艺术家对热爱的演绎事业的苦口良言。&lt;/p&gt;&lt;img src="https://pic2.zhimg.com/v2-9f62c49e54d4dd910bce8fa5820a8401_b.jpg" data-caption="" data-rawwidth="600" data-rawheight="400" class="origin_image zh-lightbox-thumb" width="600" data-original="https://pic2.zhimg.com/v2-9f62c49e54d4dd910bce8fa5820a8401_r.jpg"&gt;&lt;p&gt;&lt;b&gt;我们真的应该感谢这些坚持的戏剧、坚持热爱的演员们&lt;/b&gt;，正是因为有他们在，我们才能在烂片横行的时代还有好剧可看，在浮躁社会里，他们是真正的清流。&lt;b&gt;星星之火可以燎原，清流总有一天会变成洪流。&lt;/b&gt;&lt;/p&gt;&lt;p&gt;&lt;/p&gt;</textarea>

